    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //Step 1: Get session admin and check.
        HttpSession session = request.getSession(true);
        Account acc = (Account) session.getAttribute("admin");
        if (acc == null){
            PrintWriter out = response.getWriter();
            out.println("Access denied");
        }else{
        //Step 1: Create List of All Account
        AdminDAO admindao = new AdminDAO();
        List<Account> listaccount = admindao.GetAllAccount();
        //Step 2: Push listaccount to listuser
        request.setAttribute("listaccount", listaccount);
        //Step 3: Forward to listuser.jsp
        request.getRequestDispatcher("ListUser.jsp").forward(request, response);
        }
        
    }

